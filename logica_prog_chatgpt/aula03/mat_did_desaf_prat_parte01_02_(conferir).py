        # Escreva um programa que use um laço While para exibir todos os números ímpares de 1 a 50.

# Benefícios: praticar o uso de operadores aritméticos em Python e entender como realizar cálculos matemáticos
            # básicos com variáveis.

num = 0

while num <= 50:
    num += 1

    if num % 2 != 0:
        print(num)
    
    if num == 50:
        break


