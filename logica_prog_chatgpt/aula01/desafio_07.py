# Faça um programa que peça para o usuário digitar dois números e depois imprima a soma deles, por exemplo:

    # Digite o primeiro número: 10
    # Digite o segundo número: 5
    # ----------------------------
    # 10 + 5 = 15

n1 = int(input('Digite o primeiro número: '))
n2 = int(input('Digite o segundo número: '))

print(f'{n1} + {n2} = {n1 + n2}')