    # Verificar par ou ímpar:
        # Crie um programa que peça ao usuário um número e use a estrutura condicional if para verificar se ele é par ou ímpar.

numero = int(input('Insira um número: '))

if numero % 2 == 0:
    print('Par')
else:
    print('Ímpar')
