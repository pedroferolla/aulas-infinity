# 5 - Escreva um programa que verifica se dois números digitados pelo usuário são diferentes entre si, e imprima o resultado.

num1 = int(input('Digite o primeiro número: '))
num2 = int(input('Digite o segundo número: '))

if num1 == num2:
    print('Números iguais.')
else:
    print('Números diferentes.')
