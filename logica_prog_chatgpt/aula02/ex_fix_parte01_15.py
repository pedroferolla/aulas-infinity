# 15 - Leia dois números inteiros do usuário e exiba o primeiro número elevado ao segundo número.

# RESULTADO ESPERADO:
    # Digite o primeiro número inteiro: 3
    # Digite o segundo número inteiro: 4
    # O número 3 elevado a 4 é: 81

num1 = int(input('Digite o primeiro número inteiro: '))
num2 = int(input('Digite o segundo número: '))

print(f'O número {num1} elevado a {num2} é: {num1 ** num2}')
