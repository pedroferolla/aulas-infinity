# Escreva um programa que receba duas listas e calcule a união dos elementos únicos dessas listas, usando conjuntos.

a = {1, 2, 3, 4, 5}
b = {1, 3, 5, 7, 9}

print(a.union(b))   # |
                    # | Duas formas diferentes para união. Obs: 'union()' é feito a partir do comando 'print()'.
print(a | b)        # |
